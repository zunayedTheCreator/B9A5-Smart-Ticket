function addClassById(elementId, value) {
    const element = document.getElementById(elementId);
    element.classList.toggle(value);
}
function addSelectedClassById(elementId, value) {
    const element = document.getElementById(elementId);
    element.classList.toggle(value);
}

function removeClassById(elementId, value) {
    const element = document.getElementById(elementId);
    element.classList.toggle(value);
}